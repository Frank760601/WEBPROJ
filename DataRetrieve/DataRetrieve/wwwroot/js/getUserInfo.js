function showLoading() {
    var i = document.getElementById("loading");
    i.style.display = "block";
}

function hideLoading() {
    var i = document.getElementById("loading");
    i.style.display = "none";
}
function showMask() {
    var i = document.getElementById("maskeffect");
    i.style.display = "block";
    i.style.position = "fixed";
}

function hideMask() {
    var i = document.getElementById("maskeffect");
    i.style.display = "none";
}